#include<iostream>
#include<math.h>
#include<stdlib.h>
using namespace std;
int main()
{
    cout<<"absolute value : "<<abs(-15)<<endl;
    cout<<"value :"<<ceil(15.4)<<endl;
    cout<<"value :"<<ceil(15.8)<<endl;
    cout<<"value :"<<floor(5.4)<<endl;
    cout<<"value :"<<floor(5.8)<<endl;
    cout<<"value :"<<round(5.4)<<endl;
    cout<<"value :"<<round(5.8)<<endl;
    cout<<"value :"<<sqrt(64)<<endl;
    cout<<"value :"<<sqrt(10)<<endl;
    cout<<"value :"<<pow(2,5)<<endl;
    cout<<"value :"<<pow(10,3)<<endl;
    cout<<"value :"<<rand()%40<<endl;

}